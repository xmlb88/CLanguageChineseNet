#include <stdio.h>

//通过class关键字定义类
class Student {
public:
	//类包含的变量
	char* name;
	int age;
	float score;

	//类包含的函数
	void display() {
		printf("%s的年龄是 %d，成绩是 %.2f\n", name, age, score);
	}
};

int main() {
	//通过类来定义变量，即创建对象
	class Student stu1;
	//为类的成员变量赋值
	char a[] = "小明";
	stu1.name = a;
	stu1.age = 15;
	stu1.score = 92.5f;
	//调用类的成员函数
	stu1.display();

	return 0;
}